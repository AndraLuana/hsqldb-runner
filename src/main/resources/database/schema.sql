DROP TABLE employees IF EXISTS;
CREATE TABLE employees (
    id int GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
    name VARCHAR(255),
    email VARCHAR(255)
);

DROP TABLE phonebook IF EXISTS;
CREATE TABLE phonebook (
    id int GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
    name VARCHAR(100),
    number VARCHAR(100),
    note VARCHAR(100)
);
